package com.company;

public class Cat {
    private String name;
    private String color;
    private int age;
    private int height;

    void setName(String catName) {
        name = catName;
    }

    void setColor(String catColor) {
        color = catColor;
    }

    void setAge(int catAge) {
        age = catAge;
    }

    void setHeight(int catHeight) {
        height = catHeight;
    }

    String getName() {
        return name;
    }

    String getColor() {
        return color;
    }

    int getAge() {
        return age;
    }


    int getHeight() {
        return height;
    }


}
